package ro.unibuc.pao.types;

public class Primitives {
    // 8 bit
    byte b1 = 10;
    byte b2 = 023;

    // 16 bit
    short s1 = 10;
    short s2 = 0xFF;

    //32 bit; 4 bytes
    int i1 = 10;
    int i2 = 0b10110;

    //64 bit; 8 bytes
    long l1 = 10;

    /* Floating point - can only be in decimal form */
    // 32 bit floating point; In java orice val cu virgula e by default considerata de tip double

    float f1 = 123.45f;

    //64 bit
    double d1 = 123.456;

    //16 bit
    char c1 = 'a';
    char c2 = '\n';

    boolean k1 = false;
    boolean k2 = true;


}
